package model;

public class Coordinates {

    private int xCoordinate;
    private int yCoordinate;
    public Coordinates(int x, int y){

        this.xCoordinate = x;
        this.yCoordinate = y;
    }

    public int getXCoordinate() {
        return xCoordinate;
    }

    public int getYCoordinate() {
        return yCoordinate;
    }
}
